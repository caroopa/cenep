            <p class="subtitulo">Resumen: </p>
            <p>Se trata de un proyecto exploratorio desarrollado en forma colaborativa con la Universidad de Michigan para recolectar informaci&oacute;n que permita evaluar la forma como los argentinos conceptualizan desarrollo, progreso y modernidad y en qu&eacute; medida utilizan dichas ideas para evaluar las sociedades en general, y las organizaciones familiares en particular. Estas nociones forman parte del &quot;idealismo de desarrollo&quot; que Thornton (coordinador del proyecto) discute en profundidad en su libro Reading History Sideways: The Fallacy and Enduring Impact of the Developmental Paradigm on Family Life, publicado en el a&ntilde;o 2005 por University of Chicago Press. El paradigma del desarrollo y la lectura hist&oacute;rica &quot;de costado&quot; tanto en la evaluaci&oacute;n de sociedades y sistemas familiares utilizados por m&aacute;s de dos siglos, no fueron s&oacute;lo ideas o aproximaciones utilizados por varias generaciones de estudiosos, sino que han sido combinados en un sistema de ideas &ndash;al que Thornton denomina idealismo del desarrollo&ndash; que identifica objetivos en la vida, los medios para evaluar la vida familiar y un marco que identifica influencias causales entre la familia y la vida social y econ&oacute;mica, para guiar y motivar el cambio social, valorando la sociedad moderna y el desarrollo, la organizaci&oacute;n familiar moderna, y especificando una causalidad bidireccional entre ellas. Thornton argumenta que las proposiciones del idealismo del desarrollo han sido ampliamente diseminadas por el mundo y han sido especialmente importantes en los cambios familiares observados en distintos pa&iacute;ses del mundo. </p>
