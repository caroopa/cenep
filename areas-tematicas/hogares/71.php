            <p>Se trata de un estudio cualitativo sobre las actividades y estrategias de los hogares para enfrentar la crisis econ&oacute;mica. Entre agosto y octubre del 2000 se realizaron 35 entrevistas en profundidad e historias de vida laboral y familiar a mujeres y varones (miembros de parejas conyugales en hogares nucleares completos, con hijos a cargo) y a mujeres sin c&oacute;nyuge que encabezan hogares monoparentales. Los hogares, localizados en la ciudad de Buenos Aires y en algunos partidos del conurbano, pertenecen a estratos sociales bajos y medio-bajos. Las entrevistas estuvieron focalizadas en las actividades y los mecanismos de obtenci&oacute;n y asignaci&oacute;n de recursos monetarios y no monetarios y en las relaciones intra-familiares con foco en las relaciones de g&eacute;nero; las historias de vida se centraron en las trayectorias familiares y laborales. </p>
            <p>Las estrategias familiares de afrontamiento a la crisis econ&oacute;mica incluyeron la movilizaci&oacute;n de los diferentes miembros del hogar al mercado de trabajo, la intensificaci&oacute;n y diversificaci&oacute;n del trabajo remunerado de los proveedores habituales, la contracci&oacute;n del consumo, diversos mecanismos de ayuda solidaria y el acceso a planes sociales y programas de ONGs. Las regularidades y las diversidades de situaciones, elecciones y comportamientos se derivan de la interacci&oacute;n entre oportunidades, recursos y constricciones que operan a nivel microsocial (hogares e individuos, otros hogares) y a nivel meso y macrosocial (mercado laboral, pol&iacute;ticas sociales y econ&oacute;micas, programas sociales, instituciones). Esto apunta a la necesidad de asignar tanto recursos econ&oacute;micos como creatividad al dise&ntilde;o e implementaci&oacute;n de pol&iacute;ticas sociales bien informadas, con el objetivo de enmendar la actual privaci&oacute;n y prevenir la reproducci&oacute;n de la pobreza.</p>
