                        <p><span class="subtitulo">Investigadores responsables:</span> Sonia M. Mychaszula (directora del proyecto); Jorge L. Somoza (asesor) y Mercedes Biagioni (asistente de investigaci&oacute;n)</p>
                        <p class="subtitulo">Resumen:</p>
                        <p>El objetivo del proyecto ha sido ensayar un procedimiento que permite estimar la incidencia de personas con discapacidad y conocer sus caracter&iacute;sticas sociodemogr&aacute;ficas y necesidades. El procedimiento usa la red de escuelas primarias para acceder a una muestra de hogares. Quedan exclu&iacute;dos aqu&eacute;llos sin representaci&oacute;n en esas escuelas (limitaci&oacute;n del procedimiento). Su ventaja es que permite acceder f&aacute;cilmente a ni&ntilde;os con discapacidad (los propios alumnos o su hermanos), es decir a poblaci&oacute;n en edades en que la prevalencia de la discapacidad es relativamente baja y principalmente asociadas a deficiencias cong&eacute;nitas o adquiridas en el parto o muy cerca del nacimiento. </p>
                        <p>La encuesta, en dos etapas, se realiz&oacute; sobre los hogares de los alumnos de 5&ordm;, 6&ordm; y 7&ordm; grados de escuelas seleccionadas entre todas las primarias comunes del sector p&uacute;blico, dependientes del &Aacute;rea de Educaci&oacute;n Primaria de la Secretar&iacute;a de Educaci&oacute;n del Gobierno de la Ciudad. La recolecci&oacute;n de datos estuvo a cargo de alumnos avanzados de la carrera de Sociolog&iacute;a de la Facultad de Ciencias Sociales de la UBA.</p>
                        <p>En la primera etapa, los encuestadores distribuyeron un cuestionario dirigido a los jefes/as de hogar de los alumnos seleccionados, a efectos de detectar a las personas con discapacidad residentes en esos hogares y conocer algunas de sus caracter&iacute;sticas (sexo, edad y tipo y naturaleza de la discapacidad). Se defini&oacute; discapacidad &quot;cualquier limitaci&oacute;n f&iacute;sica, mental o sensorial (ceguera o sordera) de car&aacute;cter permanente, cong&eacute;nita (de nacimiento) o adquirida (por enfermedad, accidente, etc.)&quot;. Para calcular la incidencia de la discapacidad en la poblaci&oacute;n y caracterizar los hogares, se solicit&oacute; el tama&ntilde;o del hogar y el sexo, edad, nivel de instrucci&oacute;n y condici&oacute;n de actividad y ocupaci&oacute;n del jefe/a. En la segunda etapa, los encuestadores visitaron los hogares con al menos una persona con discapacidad, a fin de recoger los datos para caracterizar a esas personas seg&uacute;n inserci&oacute;n en el sistema escolar, nivel de instrucci&oacute;n y participaci&oacute;n en el mercado de trabajo y conocer las ayudas requeridas para atenuar sus limitaciones, sus dificultades en diferentes &aacute;mbitos y los beneficios percibidos. </p>
                        <p>El proyecto concluy&oacute; en 2001 y cont&oacute; con financiaci&oacute;n del Gobierno de la Ciudad de Buenos Aires, Secretar&iacute;a de Promoci&oacute;n Social, Tercer Sector y Nuevas Formas de Gesti&oacute;n Social (concursada).</p>
