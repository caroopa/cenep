                        <p class="subtitulo">Resumen:</p>
                        <p>La ley 24.241, sancionada en 1993, reform&oacute; el sistema previsional argentino, creando el Sistema Integrado de Jubilaciones y Pensiones (SIJP). Se trata de un sistema mixto cuya principal novedad es la capitalizaci&oacute;n de parte de los aportes jubilatorios en cuentas individuales. Con esta reforma naci&oacute; la posibilidad, y la necesidad, de proyectar la evoluci&oacute;n del sistema en el largo plazo, para analizar su sustentabilidad. De eso se trata esta investigaci&oacute;n, que se realiz&oacute; en colaboraci&oacute;n con la Facultad de Ciencias Econ&oacute;micas de la UBA para la Administraci&oacute;n Nacional de Seguridad Social (ANSeS). </p>
                        <p>Sus objetivos fueron generar proyecciones demogr&aacute;ficas (por sexo, edades simples y a&ntilde;os calendarios entre 1995 y 2050), econ&oacute;micas y financieras. A tal fin se fueron desarrollando actividades en forma correlativa, en siete etapas. La primera incluy&oacute; la preparaci&oacute;n de las proyecciones de poblaci&oacute;n por edades simples y a&ntilde;os calendarios, seg&uacute;n sexo, hasta el a&ntilde;o 2050. En la segunda y tercera etapas se estimaron y proyectaron hasta 2050, la estructura socioecon&oacute;mica de la poblaci&oacute;n, la proporci&oacute;n de aportantes, de trabajadores en relaci&oacute;n de dependencia y de trabajadores en uno u otro r&eacute;gimen seg&uacute;n categor&iacute;a ocupacional, todo por edades simples. En la cuarta etapa se obtuvieron las correspondientes estimaciones de poblaci&oacute;n. En la quinta etapa se estimaron las variables financieras (salarios y rentabilidad del sistema) mientras que en la sexta y s&eacute;ptima etapas se procedi&oacute; a integrar la totalidad de las proyecciones y modelar supuestos alternativos. </p>
                        <p>Este proyecto gener&oacute; la primera versi&oacute;n del &quot;SINPRA&quot; (Sistema de Informaci&oacute;n Previsional de Argentina), primer modelo integrado de informaci&oacute;n demogr&aacute;fica y financiera del R&eacute;gimen Previsional Publico en el pa&iacute;s. finalizado en 1996, cont&oacute; con financiaci&oacute;n de la ANSES y la Facultad de Ciencias Econ&oacute;micas, UBA. </p>
