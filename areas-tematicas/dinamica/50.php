                        <p><span class="subtitulo">Investigadores responsables:</span> Edith A. Pantelides; Rafael Rofman; Mar&iacute;a S. M&uuml;ller; Mariano Sana; Julieta Oddone (CEIL); Adriana Fassio (UBA)</p>
                        <p class="subtitulo">Resumen:</p>
                        <p>Este proyecto, realizado en colaboraci&oacute;n con investigadores del CEIL y la UBA, tuvo por objetivo caracterizar la pobreza en la tercera edad en la Argentina (en jurisdicciones seleccionadas para representar la diversidad de situaciones). La medici&oacute;n de la pobreza se realiz&oacute; seg&uacute;n indicadores de necesidades b&aacute;sicas insatisfechas (NBI) y de l&iacute;nea de pobreza (LP), utilizando datos censales y de la Encuesta Permanente de Hogares (s&oacute;lo para el Gran Buenos Aires). Se hicieron adem&aacute;s estudios de casos.</p>
                        <p>Para su consecuci&oacute;n fue necesario hacer un an&aacute;lisis cr&iacute;tico de los indicadores mencionados, que concluy&oacute; en la recomendaci&oacute;n de no usar las NBI para medir la pobreza en la vejez. Asimismo, se dedic&oacute; un cap&iacute;tulo especial al estudio del impacto de la pobreza en las mujeres y otro a las transferencias del resto de la sociedad hacia la tercera edad. El estudio concluy&oacute; con un an&aacute;lisis de las pol&iacute;ticas existentes dirigidas a la tercera edad.</p>
                        <p>La investigaci&oacute;n mostr&oacute; que una proporci&oacute;n de los ancianos, que llega al 50 por ciento en las jurisdicciones de menor desarrollo relativo, tiene problemas habitacionales y sanitarios importantes. La carencia m&aacute;s frecuente es la de agua potable dentro de la vivienda.</p>
                        <p>Si bien las mujeres son la mayor&iacute;a de la poblaci&oacute;n anciana, no lo son con relaci&oacute;n a la poblaci&oacute;n pobre definida seg&uacute;n NBI. Sin embargo, la investigaci&oacute;n cualitativa muestra que podr&iacute;an sufrir desventajas adicionales no reflejadas en sus NBI, en especial dada la mayor incidencia entre ellas de la viudez y de la residencia unipersonal, que las hace m&aacute;s vulnerables. Es interesante observar que, por otro lado, la viudez puede elevar a las mujeres por encima de la LP, al recibir la pensi&oacute;n de los esposos y existir un miembro menos del hogar.</p>
                        <p>Se observa una mayor incidencia de las NBI en los ancianos m&aacute;s j&oacute;venes, lo que se proyectar&iacute;a en una desmejora de la situaci&oacute;n de la poblaci&oacute;n anciana en general en el futuro.</p>
                        <p>En el Gran Buenos Aires los ancianos m&aacute;s necesitados (medido seg&uacute;n LP, con informaci&oacute;n proveniente de la EPH) son los que no perciben beneficio previsional y no tienen otros ingresos. Respecto a los subsidios, como la mayor parte de ellos requiere ser beneficiario del sistema previsional, quedan excluidos aquellos que tienen mayor necesidad de auxilio. En general, el efecto logrado no es conmensurado con el volumen de gasto incurrido.</p>
