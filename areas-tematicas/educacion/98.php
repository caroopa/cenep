              3) Evaluar las aspiraciones educativas de los alumnos que transitan el &uacute;ltimo a&ntilde;o del ciclo medio, en conjunci&oacute;n con la informaci&oacute;n de la que disponen para llevarlas a cabo exitosamente. En este caso nos proponemos examinar si los alumnos planean o no continuar su educaci&oacute;n post-secundaria, las motivaciones y el sentido que otorgan a sus decisiones educativas, como as&iacute; tambi&eacute;n las diferentes fuentes de informaci&oacute;n y modelos en que basan sus planes y aspiraciones. <br>
              4) Analizar, desde la perspectiva de los propios j&oacute;venes de nivel socioecon&oacute;mico medio-bajo, qu&eacute; tipo de experiencias durante sus estudios secundarios han contribuido, facilitado y sido aprovechadas durante sus trayectorias educativas posteriores.</p>
