            <p>1) Examinar si las escuelas secundarias que atienden a poblaci&oacute;n de nivel socioecon&oacute;mico medio bajo mantienen pr&aacute;cticas para estimular, fomentar y facilitar la continuidad de sus estudiantes al ciclo superior. M&aacute;s precisamente nos proponemos describir los diferentes tipos de acciones formales e informales y las caracter&iacute;sticas de implementaci&oacute;n de las diferentes pr&aacute;cticas llevadas a cabo por la escuela o actores espec&iacute;ficos. <br>
