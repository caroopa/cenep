                        <p class="subtitulo">Resumen: </p>
                        <p>La reforma educativa argentina de los a&ntilde;os noventa implic&oacute; cambios estructurales y curriculares: los establecimientos educativos no universitarios nacionales pasaron a la jurisdicci&oacute;n provincial y se modificaron la estructura de los cursos y los planes de estudio. Esta investigaci&oacute;n enfatiza el nivel institucional como clave para la innovaci&oacute;n educativa y la mejora de la equidad.</p>
                        <p>El primer componente del trabajo, de &iacute;ndole cualitativa, indaga sobre los grados de libertad que tienen las escuelas secundarias p&uacute;blicas y privadas (confesionales y no confesionales) en sus decisiones, el impacto de &eacute;stas en la vida escolar, las relaciones entre los estratos escolares y la visi&oacute;n que tienen estos protagonistas sobre los objetivos y resultados de la escuela. Finalmente, se analiza si las diferencias permiten instrumentar modelos escolares exitosos y cu&aacute;les son los obst&aacute;culos internos y externos para ello. Se basa en una muestra cualitativa de escuelas secundarias de gesti&oacute;n p&uacute;blica y privada, que var&iacute;an en tama&ntilde;o, propiedad, y nivel socioecon&oacute;mico de los alumnos. En ellas se realizan entrevistas en profundidad a directivos y docentes, consultas a informantes clave, reelabor&aacute;ndose asimismo informaci&oacute;n recabada en estudios anteriores.</p>
                        <p>El segundo componente de esta investigaci&oacute;n, cuantitativo, tiene por prop&oacute;sito examinar dos aspectos del rendimiento educativo: la repitencia y la deserci&oacute;n en el nivel secundario en relaci&oacute;n con el tipo de establecimiento (p&uacute;blico, privado no confesional y privado confesional). Los resultados indican que los estudiantes en escuelas privadas tienen no s&oacute;lo menores chances de repetir sino tambi&eacute;n de desertar que sus pares que asisten a establecimientos p&uacute;blicos. Las caracter&iacute;sticas socioecon&oacute;micas y familiares de los estudiantes dan cuenta s&oacute;lo en parte del mejor rendimiento en las escuelas privadas y las brechas entre ambos tipos de establecimientos persisten luego de mantener constante el efecto de dichos factores.</p>
                        <p>Los resultados de esta investigaci&oacute;n evidencian las diferencias en los modelos institucionales entre escuelas privadas y p&uacute;blicas del nivel medio y analizan las condiciones necesarias para el desarrollo de un proyecto institucional, brindando as&iacute; propuestas concretas de pol&iacute;ticas p&uacute;blicas que tiendan a mejorar la calidad y equidad del sistema educativo medio Argentino.</p>
