                        <p>Partiendo de la identificaci&oacute;n de los hogares que ten&iacute;an a una mujer como principal perceptora de ingresos, este trabajo estuvo centrado en el an&aacute;lisis (cuantitativo y cualitativo) de las estructuras y la din&aacute;mica de la vida cotidiana de familias que escapan al modelo predominante de familia compuesta por un matrimonio en el que el &quot;jefe&quot; var&oacute;n desempe&ntilde;a el rol de proveedor econ&oacute;mico y la &quot;c&oacute;nyuge&quot; mujer, las tareas dom&eacute;sticas ligadas a la procreaci&oacute;n y a la reproducci&oacute;n cotidiana. </p>
