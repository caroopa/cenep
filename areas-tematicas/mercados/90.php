                        <p class="subtitulo">Resumen:</p>
                        <p>Esta investigaci&oacute;n ha tenido car&aacute;cter exploratorio por tratarse de un tema pr&aacute;cticamente indocumentado. Se acudi&oacute; a una variedad de fuentes cuantitativas y cualitativas: series estad&iacute;sticas oficiales publicadas e in&eacute;ditas; censos y encuestas de hogares; entrevistas focalizadas a un corto n&uacute;mero de trabajadores y sindicalistas; entrevistas a informantes calificados.</p>
                        <p>Las gruesas estimaciones obtenidas a partir de las escasas estad&iacute;sticas disponibles sugerir&iacute;an que el desempleo debido a la privatizaci&oacute;n de empresas estatales tuvo un sesgo de g&eacute;nero perjudicial para las mujeres. A fines de 1993, m&aacute;s de 78 mil trabajadores hab&iacute;an perdido sus puestos como consecuencia de los procesos previos y posteriores a la privatizaci&oacute;n de empresas estatales. Ello representa casi el 30 % de la fuerza de trabajo empleada en las empresas estatales en 1990; aproximadamente el 23 % de ellos corresponde a mujeres. </p>
                        <p>Tambi&eacute;n se llev&oacute; a cabo un estudio de casos sobre los efectos de los cambios introducidos en las condiciones de empleo y de trabajo sobre las mujeres empleadas en las dos empresas de servicios privatizadas que en 1985 contaban con la mayor proporci&oacute;n de mujeres entre sus planteles de empleados: la ex Entel y Aerol&iacute;neas Argentinas. Las nuevas y m&aacute;s duras condiciones de empleo y de trabajo significaron una disminuci&oacute;n en su calidad de vida y en ocasiones tambi&eacute;n en sus remuneraciones, especialmente para las que ten&iacute;an responsabilidades familiares dif&iacute;ciles de compatibilizar con las demandas laborales y cuyas necesidades estuvieron mejor protegidas en el &aacute;mbito estatal.</p>
                        <p>Esta investigaci&oacute;n, concluida en 1997, representa probablemente el primer intento de estimaci&oacute;n del n&uacute;mero total de puestos de trabajo perdidos a consecuencia del proceso de privatizaci&oacute;n de empresas p&uacute;blicas en el pa&iacute;s y es, sin duda, el primero en presentar informaci&oacute;n concerniente a las mujeres que trabajan en empresas privatizadas.</p>
