                        <p class="subtitulo">Resumen:</p>
                        <p>La investigaci&oacute;n abord&oacute; el caso argentino como parte de un estudio comparativo en seis pa&iacute;ses de Am&eacute;rica Latina (Argentina, Chile, Per&uacute;, Uruguay, M&eacute;xico y Brasil) y tuvo como prop&oacute;sito describir las dr&aacute;sticas transformaciones experimentadas por la sociedad y las diversas respuestas que la sociedad civil ha ido delineando a lo largo de dicho proceso en el &Aacute;rea Metropolitana de Buenos Aires. </p>
                        <p>Un primer componente de la investigaci&oacute;n (a cargo de Marcela Cerrutti), examina los efectos de las pol&iacute;ticas neoliberales en la capacidad de la estructura productiva para generar trabajo y, consecuentemente, ingresos para sus pobladores, en la expresi&oacute;n espacial de los cambios en la estructura de oportunidades y en las diferentes formas de reacci&oacute;n de la poblaci&oacute;n. Se abordan los cambios en el modelo econ&oacute;mico e institucional y sus efectos sociales. En este sentido, se presentan las principales tendencias del mercado de trabajo, especialmente aquellas referidas al desempleo, la precarizaci&oacute;n del empleo y la evoluci&oacute;n del sector informal, y se analizan los impactos de dichas transformaciones en la distribuci&oacute;n del ingreso y la extensi&oacute;n de la pobreza e indigencia y en la segmentaci&oacute;n espacial de la poblaci&oacute;n. </p>
                        <p>Un segundo componente de la investigaci&oacute;n (a cargo de Alejandro Grimson), aborda mediante una perspectiva etnogr&aacute;fica, las diferentes respuestas de la sociedad civil a estas grandes transformaciones. Espec&iacute;ficamente se analizan los cambios en la agenda de los sectores populares, sus distintos tipos de organizaciones, formas de identificaci&oacute;n y modos de acci&oacute;n.<br>
