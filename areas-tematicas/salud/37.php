            <p><span class="subtitulo">Investigadores responsables:</span> F&eacute;lix Abdala, Rosa N. Geldstein y Sonia M. Mychaszula</p>
            <p class="subtitulo">Resumen:</p>
            <p>La investigaci&oacute;n form&oacute; parte de un proyecto multinacional comparativo cuyo principal objetivo fue analizar el impacto demogr&aacute;fico de cambios econ&oacute;micos profundos y r&aacute;pidos ocurridos en pa&iacute;ses de Europa Oriental (ex-bloque sovi&eacute;tico), la Federaci&oacute;n Rusia y dos pa&iacute;ses latinoamericanos (Argentina y M&eacute;xico). Esta investigaci&oacute;n puso a prueba la relaci&oacute;n entre la hiperinflaci&oacute;n de fines de los a&ntilde;os 80, el creciente desempleo de comienzos de los a&ntilde;os 90 y las causas de mortalidad para grupos poblacionales espec&iacute;ficos. Se hizo uso de series de tiempo de variables demogr&aacute;ficas, econ&oacute;micas y ocupacionales y diversos modelos de regresi&oacute;n (an&aacute;lisis factorial).</p>
            <p>Resultados obtenidos: la hip&oacute;tesis central del proyecto, basada en la generalizada experiencia del ex bloque sovi&eacute;tico, entend&iacute;a al r&aacute;pido colapso del sistema social como la causa principal del brusco empeoramiento de las condiciones de vida, aumento de la mortalidad en general y de los hombres activos en particular debido al aumento del stress psicosocial y de las enfermedades cardiovasculares. Esta hip&oacute;tesis no fue verificada para el caso argentino en el que, en cambio, uno de los hallazgos m&aacute;s destacados fue el aumento del nivel de mortalidad de los hombres j&oacute;venes adultos debido a enfermedades infecto-contagiosas y no a enfermedades cardiovasculares o violentas m&aacute;s relacionadas con las crisis socioecon&oacute;micas. Los resultados obtenidos, sin embargo, deben considerarse provisorios debido, en gran medida, a la no disponibilidad de datos m&aacute;s adecuados para poner a prueba la hip&oacute;tesis originaria: por mencionar un ejemplo, las series de datos usados por esta investigaci&oacute;n solo llegaron hasta el momento en que el proceso de creciente (y alto) desempleo estaba en sus comienzos y, junto con &eacute;l, la disrupci&oacute;n social y sus concomitantes efectos demogr&aacute;ficos.</p>
