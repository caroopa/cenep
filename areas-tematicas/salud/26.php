            <p class="subtitulo">Resumen: </p>
            <p>Esta investigaci&oacute;n form&oacute; parte de un programa m&aacute;s amplio desarrollado en el CENEP para medir la mortalidad infantil mediante un m&eacute;todo ideado por Brass (Brass; W. Y Macrae, S., 1985. &quot;Childhood mortality estimated from reports on previous birth given by mothers at time of maternity. I-Preceding birth technique&quot;, en Advances in Methods for Estimating Fertility and Mortality from Limited and Defective Data. Centre for Population Studies, Londres), que se apoya en informaci&oacute;n que proporcionan las mujeres que concurren a un centro de salud a dar a luz, acerca de la sobrevivencia o muerte del hijo nacido vivo inmediato anterior -hijo previo- al que est&aacute;n en trance de tener.</p>
            <p>Su desarrollo obedeci&oacute; al inter&eacute;s del Dr. Carlos Donatti por contar con una estimaci&oacute;n de la mortalidad infantil de los hijos de mujeres que acud&iacute;an a dar a luz al Hospital Zonal de Z&aacute;rate. Las parteras se ocuparon de recoger la informaci&oacute;n. El trabajo conjunto permiti&oacute; ir modificando el cuestionario inicial a fin de mejorar la calidad de la informaci&oacute;n para medir la mortalidad infantil y ampliar el conocimiento de las madres y sus hijos (peso al nacer y semanas de gestaci&oacute;n del hijo actual, total de hijos nacidos muertos tenidos hasta el momento del parto actual, cantidad de controles prenatales). Tal como fue ideado, el procedimiento se apoya en informaci&oacute;n sobre los hijos previos de hijos actuales nacidos vivos exclusivamente; en Z&aacute;rate comenz&oacute; a recogerse informaci&oacute;n sobre los hijos actuales nacidos muertos para medir la incidencia de los mortinatos (nacidos muertos de 28 y m&aacute;s semanas de gestaci&oacute;n) en el total de partos atendidos en el hospital.</p>
            <p>Los resultados entusiasmaron a m&eacute;dicos y dem&oacute;grafos de la Argentina y otros pa&iacute;ses de Am&eacute;rica Latina, para aplicar el procedimiento que brinda informaci&oacute;n &uacute;til para tomar decisiones en materia de salud materno-infantil a nivel local y programar los servicios de ginecolog&iacute;a y obstetricia de un hospital seg&uacute;n las caracter&iacute;sticas de la demanda.</p>
            <p>Concluido en 1995, este proyecto de investigaci&oacute;n cont&oacute; con subsidio de la Swedish Agency for Research Cooperation with Developing Countries &ndash; SAREC y del CENEP.</p>
