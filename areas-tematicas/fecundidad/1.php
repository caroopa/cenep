                        <p class="subtitulo">Resumen:</p>
                        <p>La Encuesta Nacional de Nutrici&oacute;n y Salud (ENNyS), realizada por el Ministerio de Salud de la Naci&oacute;n, ha dado la oportunidad de contar con informaci&oacute;n sobre aspectos de salud sexual y reproductiva de las mujeres argentinas hasta ahora no disponibles. La encuesta se realiz&oacute; entre 2004 y 2005 a muestras representativas de mujeres no embarazadas y embarazadas de 10 a 49 a&ntilde;os, residentes en localidades de 5000 y m&aacute;s habitantes.</p>
                        <p>Los resultados abordan las siguientes dimensiones: 1) la historia reproductiva de las mujeres, incluyendo la iniciaci&oacute;n sexual, la fecundidad, p&eacute;rdidas de embarazos y el control y prevenci&oacute;n durante el &uacute;ltimo embarazo; 2) las caracter&iacute;sticas del parto, entendiendo por ello el tipo de parto (vaginal o por ces&aacute;rea) y el subsector del sistema de salud en que el parto fue atendido; 3) el conocimiento de m&eacute;todos anticonceptivos; 4) el momento de iniciaci&oacute;n del uso de anticonceptivos, en relaci&oacute;n a la edad de la mujer y al n&uacute;mero de hijos tenidos; 5) el uso actual de anticonceptivos entre las mujeres sexualmente activas, incluyendo qui&eacute;n tomo la decisi&oacute;n de utilizar el m&eacute;todo, la fuente de informaci&oacute;n sobre el mismo, el lugar en que fue obtenido y la gratuidad (o no) del m&eacute;todo anticonceptivo utilizado; 6) los motivos por los cuales las mujeres no utilizan m&eacute;todos anticonceptivos; y 7) la prevenci&oacute;n del c&aacute;ncer de cuello de &uacute;tero y de mama, para lo cual se analiza la realizaci&oacute;n de Papanicolau, colposcop&iacute;a, mamograf&iacute;a y autoexamen de mamas.</p>
