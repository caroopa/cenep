						<p class="subtitulo">Resumen:</p>
						<p>Investigaci&oacute;n emp&iacute;rica cuyo objetivo es evaluar el grado de correspondencia entre la informaci&oacute;n estad&iacute;stica sobre salud sexual y reproductiva (SSyR) disponible en Argentina (ciudad y provincia de Buenos Aires)) desde mediados de los a&ntilde;os 1990s y el conjunto de indicadores de SSyR propuesto por la Organizaci&oacute;n Mundial de la Salud en su liminar documento, <em>Accelerating progress toward the attainment of international goals of reproductive health. </em><em>A framework for implementing the WHO Global Reproductive Health Survey,</em> Geneva, 2007.</p>
						<p>La investigaci&oacute;n consider&oacute; importante, por un lado, incluir a todos los indicadores del documento porque los mismos cubren la gran mayor&iacute;a (sino todos) los aspectos relevantes de la SSyR y las formas de medirlos y, por el otro, hacer un an&aacute;lisis metodol&oacute;gico de tales indicadores previo a la determinaci&oacute;n de su factibilidad de calculo para nuestro estudio de caso.&nbsp; Entre los hallazgos de importancia alcanzados, el primero, es que el numero de indicadores factibles de calcular con nuestros datos es grande debido, y sobre todo, a&nbsp; que los &uacute;nicos datos utilizados son secundarios: datos publicados en m&uacute;ltiples formas y datos recogidos por censos y encuestas estad&iacute;sticamente representativos de los universos de an&aacute;lisis requeridos por el&nbsp; indicador. Un segundo hallazgo de importancia es que los cambios metodol&oacute;gicos y t&eacute;cnicos realizados en el sistema de indicadores propuesto para nuestro caso ampl&iacute;an la medici&oacute;n de la salud sexual y reproductiva.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
